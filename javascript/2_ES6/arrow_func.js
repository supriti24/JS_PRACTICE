
// conventional method
function sum1(a, b) {
    return a + b;
}
console.log("addition 1 : "+sum1(2,3));

// ES6 arraow function
const sum2 = (c, d) => c + d;
console.log("addition 2 : "+sum2(3,3));

const sub = (e,f) => e-f;
console.log("subtraction : "+sub(10,3));

const mul = (g,h) => g*h;
console.log("multiplication : "+mul(15,10));

const div = (i,j) => i/j;
console.log("division : "+div(100,10));